# Atividade 1
def menor (a,b):
    if a == b:
      return "Ambos são iguais."
    elif a < b:
      return a
    else:
      return b

a = input("Digite o primeiro parâmetro:")
b = input("Digite o segundo parâmetro:")
print(menor(a,b))

